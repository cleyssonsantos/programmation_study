"""
Retorno de valores das funções (return)
"""


def soma(x, y):
    return x + y

soma1 = soma(2, 2)
soma2 = soma(3, 3)

print(soma1)
print(soma2)

def soma_tres_valores(x, y, z):
    return x + y + z

soma1 = soma_tres_valores(1, 2, 3)
soma2 = soma_tres_valores(4, 5, 6)

print(soma1)
print(soma2)
